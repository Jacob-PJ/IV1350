package src.se.kth.iv1350.sem3.integration;

/**
 * Acts as a placeholder for a real printer in a physical store.
 * Simulates the printing of a receipt by displaying it in the console.
 */
public class Printer {

    /**
     * Prints the receipt to the console.
     *
     * @param receipt The <code>String</code> representation of the receipt to
     *                print.
     */
    public void printReceipt(String receipt) {
        System.out.println(receipt);
    }
}
